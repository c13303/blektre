<p class="style6"><a name="1" id="1"></a>BLEKTRE: Qu'est ce que l'encule ? </p>
<p><br />
Blektre n'est pas un jeu de r&ocirc;le: c'est une simulation de la vraie vie, mais dans laquelle vos actes ont moins de gravit&eacute; que dans la vraie vie puisque c'est juste dans l'ordinateur. Toutefois, cela permet de vous exercer avant de vous lancer dans la vraie vie. Vous affrontez des vrais gens et la dure cruaut&eacute; de notre monde. </p>
<p>Le but de Blektre c'est d'aller au bout de ses r&ecirc;ves, d'accomplir quelquechose de grand qui restera pour la post&eacute;rit&eacute;. Pour certains, se taper Josiane est une fin en soi. Mais cela n'est pas obligatoire.</p>
