        <p> NATHALIE QUINTANE &Agrave; PROPOS DE BLEKTRE<br />
            Blektre est &agrave; l&rsquo;origine un jeu interactif sur internet au cours duquel  revient une litanie idiote et cruelle (&quot;Vous avez fum&eacute; Gore &agrave; Parly  II/Vous avez &eacute;t&eacute; fum&eacute; par Jeanne &agrave; Parly II/Vous avez rackett&eacute;  Lemon/Vous avez dragu&eacute; le patron&quot;, etc). <em><strong>Blektre</strong></em>,  la pi&egrave;ce, est rythm&eacute;e par cette litanie, mais c&rsquo;est surtout la mise en  sc&egrave;ne d&rsquo;un monde satirique et violemment po&eacute;tique qui est moins  l&rsquo;envers du n&ocirc;tre que sa translation exag&eacute;r&eacute;e. Son sc&eacute;nario po&eacute;tique ne  cesse d&rsquo;insister sur l&rsquo;aspect profond&eacute;ment pu&eacute;ril des codes et des  attendus qui nous encadrent. La construction emprunte au <em><strong>Mahagonny</strong></em> de Brecht - ruptures, acm&eacute;s, absence de hi&eacute;rarchie des m&eacute;dias (musique,  vid&eacute;o ...) - auquel certaines citations d&eacute;tourn&eacute;es renvoient &eacute;galement  quelquefois...</p>
